SELECT POSITION,
       COUNT(*)
FROM PLAYER
GROUP BY POSITION


SELECT COUNTRY_ID,
       COUNT(*)
FROM PLAYER
GROUP BY COUNTRY_ID


SELECT CLUB_ID,
       COUNT(*)
FROM PLAYER
GROUP BY CLUB_ID


SELECT POSITION,
       AVG(JERSEY_NUMBER)
FROM PLAYER
GROUP BY POSITION


SELECT COUNTRY_ID,
       MAX(BIRTH_DATE)
FROM PLAYER
GROUP BY COUNTRY_ID


SELECT POSITION,
       COUNT(*)
FROM PLAYER
WHERE POSITION <> 'Goalkeeper'
GROUP BY POSITION
ORDER BY POSITION